	  , (6, 'six'), (7, 'seven'), (8, 'eight'), (9, 'nine'), (0, 'zero') )
	as T (DIG, NUM)
),
NUMS_REV as (
	select dig, reverse(substr(NUM,1,3)) as RNUM from NUMS
),
QUE as (
	select 
		day_input
		, reverse(day_input) as reversed
		, (substring(day_input from '\d'))::NUMERIC as ResDig
		, (substring(reverse(day_input) from '\d'))::numeric as RevResDig
		, substring(        day_input   from 'one|two|thr|fou|fiv|six|sev|eig|nin|zer|\d') as ResNum
		, substring(reverse(day_input)  from 'eno|owt|rht|uof|vif|xis|ves|gie|nin|rez|\d') as ResNum_Rev
	from aoc_input m
	where daynum = 1 and input_type = 'P' 
),
CALC as (
	select  
		q.day_input, q.reversed , q.resdig, q.revresdig, f.dig as fd, b.dig as bd, 
		concat(coalesce(f.dig, q.resdig), coalesce(b.dig, q.revresdig))::DECIMAL(2,0) as res
	from que q
		left join NUMS f
			on q.resnum=f.num
		left join NUMS_REV b
			on q.ResNum_Rev=b.rnum
) select sum(RES) as result from CALC
;




